import React from "react";

import "./styles.css";
import TextBox from "../TextBox/TextBox";
import ChatStream from "../ChatStream/ChatStream";
import io from "socket.io-client";

class ChatColumn extends React.Component {
    constructor(props) {
        super(props);
        this.state = {
            message: "",
            messages: [],
            loading: true
        };
        this.server = "https://aqueous-savannah-37003.herokuapp.com";
        this.socket = io(this.server);
    }

    componentDidMount() {
        fetch(this.server)
            .then(res => res.json())
            .then(
                result => {
                    this.setState({
                        messages: result,
                        loading: false
                    });
                },
                error => {
                    console.log(error);
                }
            );

        this.socket.on("receiving", data => {
            this.setState(prevState => ({
                messages: [...prevState.messages, data]
            }));
        });
        var myName = prompt("What is your name");
        this.setState({ name: myName });
    }

    handleChange = e => {
        this.setState({ [e.target.name]: e.target.value });
    };

    handleSendMessage = () => {
        var newMessage = {
            body: this.state.message,
            name: this.state.name
        };
        this.setState(prevState => ({
            messages: [...prevState.messages, newMessage],
            message: ""
        }));
        this.socket.emit("send", newMessage);

        fetch(this.server + "/send", {
            method: "POST",
            headers: {
                "Content-Type": "application/json"
            },
            body: JSON.stringify(newMessage)
        })
            .then(res => res.json())
            .then(result => {
                console.log(result);
            });
    };


    loader = () => {
        if (this.state.loading) {
            return (
                <div>
                    <h2>Loading...</h2>
                </div>
            );
        } else {
            return [
                <ChatStream messages={this.state.messages} />,
                <TextBox
                    message={this.state.message}
                    onChange={this.handleChange}
                    onSubmit={this.handleSendMessage}
                />
            ];
        }
    };

    render() {
        return <div className="chat-area">{this.loader()}</div>;
    }
}

export default ChatColumn;